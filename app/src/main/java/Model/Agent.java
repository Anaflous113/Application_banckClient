package Model;

public class Agent {
    private Long id;
    private String nom;
    private String prenom;
    private String password;
    private String email;
    private String role;

    public Long getId() {
        return id;
    }

    public String getNom() {
        return nom;
    }

    public String getPrenom() {
        return prenom;
    }

    public String getPassword() {
        return password;
    }

    public String getEmail() {
        return email;
    }

    public String getRole() {
        return role;
    }
}
